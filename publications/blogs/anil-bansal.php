    <p class="heading-strong"><strong>What message would you like to give to IITK students who want to be entrepreneurs/investors like you?</strong></p>
    <p>
        IIT has evolved into a renowned and respected brand, recognized worldwide. Individuals who graduate from IIT are destined for success in life. However, when it comes to establishing and prospering in your own ventures, taking risks becomes imperative. Nevertheless, perseverance and consistent hard work will yield results. It's common for people to exert effort for a brief period and then give up when they don't see immediate outcomes. Instead, focus on your work without worrying about the results because they will manifest in due time.
    </p>
    <p>
        Often, many of my intelligent friends approach me after conducting extensive analyses, seeking assurance that they have considered every aspect. The truth is, it is impossible to account for every single factor! Therefore, if you possess a strong conviction, take inspiration from Nike and simply "Just Do It!" I believe I am a testament to this approach.
    </p>

    <p class="heading-strong"><strong>You were named The South Asian Times Person of the Year 2022. Your thoughts.</strong></p>
    <p>
        I am filled with a sense of pride and humility upon receiving such a prestigious honor. Previous recipients of this recognition have included notable figures such as Hardeep Singh Puri, former India's ambassador to the UN and current minister in PM Modi's cabinet, New York City Mayor Bill de Blasio, and Pramukh Swami Maharaj of BAPS. This acknowledgment not only reflects my own accomplishments but also serves as a testament to the exceptional talent, dedication, and ingenuity displayed by Indian entrepreneurs like myself.
    </p>
    
