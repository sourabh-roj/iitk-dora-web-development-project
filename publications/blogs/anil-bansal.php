    <p class="heading-strong"><strong>What are your fondest memories of IITK, and how did IITK shape your personality?</strong></p>
    <p>
        Attending IIT Kanpur in 1972 was a transformative experience for me, especially coming from a humble background. Being at IIT, it was a daily reminder, whether I believed it or not, that I was among the brightest students in the entire country. This constant affirmation boosted my confidence and deepened my respect for society.
    </p>
    <p>
        One of the greatest aspects of my time at IIT was forging lifelong friendships. In addition to receiving a full scholarship, I also took on some work for a professor, which provided me with extra income. With a generous mindset, I always took care of my peers, engaging in activities and building strong connections. Interacting with students from various regions and diverse backgrounds enriched my perspective and made me a more accepting individual. I discovered commonalities among people with different means, fostering a greater sense of unity.
    </p>

    <p class="heading-strong"><strong>If you could go back in time and meet your younger self in IITK, what would you tell him now?</strong></p>
    <p>
        Embrace it, my friend! Live in the moments, celebrate joyfully, forge friendships, and let laughter fill your days. Dedicate yourself to diligent and unwavering effort, never allowing setbacks to dampen your spirit. Cherish this present moment, for it holds the potential to become the most remarkable and cherished time of your life.
    </p>

    <p class="heading-strong"><strong>What is your favorite leisure activity?</strong></p>
    <p>
        I have a profound passion for traveling, and my wife shares the same enthusiasm. Our love for history and nature has led us to explore and experience over 100 countries. Throughout our journeys, certain destinations hold a special place in our hearts, such as our unforgettable visit to Mt Kailash-Mansarovar and our awe-inspiring expedition to Antarctica.
    </p>
    <p>
        In addition to our adventures, I also consider myself a food enthusiast and take great pleasure in entertaining others. The joy of savoring delectable cuisines and creating memorable dining experiences for guests brings me immense satisfaction.
    </p>

    <p class="heading-strong"><strong>How would you sum up your life so far?</strong></p>
    <p>
        I have been blessed with the kindness of God, the unwavering support of my family, and the encouragement of those around me. I am immensely grateful to America, which has played a significant role in enabling my achievements. The abundance I have been fortunate to possess has allowed me to contribute back to society, both locally and to my beloved motherland, India.
    </p>
    <p>
        India holds a special place in my heart, as it has instilled in me the values of humility, respect for others, and the importance of lending a helping hand. From a young age, we are taught in India that the world is a unified family, and this principle resonates deeply within me. India has exemplified this ethos, particularly during the challenging times of the Covid pandemic.
    </p>

    <p class="heading-strong"><strong>What are your future plans?</strong></p>
    <p>
        My current focus is on uplifting not just humanity as a whole, but specifically my fellow Indians. I am driven to promote their happiness, and success, and to enhance the perception of Indians on a global scale. As a Director of the Indo-American Arts Council based in New York, I actively contribute to showcasing the rich Indian arts and culture. Additionally, I am involved with the FIA (Federation of Indian Associations) New York tristate chapter, which orchestrates the largest India Day Parade outside of India each year.
    </p>
    <p>
        My philanthropic efforts extend to supporting diverse charities, all with the underlying purpose of fostering a sense of pride and confidence among fellow Indians. It is vital for us to protect and celebrate our deep-rooted Indian arts and culture, while also showcasing them to the world, particularly in America. Simultaneously, I strive to instill a sense of pride within fellow Indians, reinforcing their identities, accomplishments, and our collective worldview of Vasudhaiva Kutumbakam, where the world is viewed as one family.
    </p>
