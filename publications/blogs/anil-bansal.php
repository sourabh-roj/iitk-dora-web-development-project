    <p class="heading-strong"><strong>Your first venture was Essex Computers. What were the challenges and learnings, and how did it come about?</strong></p>
    <p>
        Upon completing my master's degree at Notre Dame University, I worked for a major corporation for the first five years. I felt unfulfilled and wanted to do it my way. Having no capital, I ventured into the retail sector, working for Computer Pros, a store specializing in Apple computers. Through dedicated efforts, I successfully turned the company's fortunes around, transforming it from a negative position to a significantly positive one. However, despite the progress, the owner went back on our agreement to make me a partner. In hindsight, this turned out to be a blessing in disguise.
    </p>
    <p>
        Motivated by the setback, I approached the executives of Apple Computers and secured a dealership from Apple in 1989. This marked the beginning of my first company, Essex Computers, based in Newark, NJ. As circumstances led us to relocate, I seized the opportunity to purchase a 20,000 sq ft building in Clifton. To make the most of the additional space, I established a flourishing desktop publishing training center. Prominent companies like Bantam Books, Conde Nast, and The New York Times began sending their employees to us for software training, particularly in programs like PageMaker. Soon, our training services generated revenue exceeding one million dollars, and we became the largest supplier of Apple computers in any location. Additionally, we secured a significant contract from the New Jersey school system to provide computer maintenance services.
    </p>
    <p>
        This journey taught me a valuable lesson: by delivering excellent service and demonstrating care for our customers, we eventually secured our rightful share of the business.
    </p>

    <p class="heading-strong"><strong>What made you shift to real estate?</strong></p>
    <p>
        As our computer business flourished, reaching a revenue milestone of $30 million, I received guidance to leverage our success by investing in real estate. Having already acquired my first building, I took the advice to heart and embarked on a ten-year journey of purchasing one building annually, focusing on commercial real estate and office buildings. This strategic move not only provided financial opportunities but also paved the way for establishing multiple businesses in the future.
    </p>
    <p>
        At present, we proudly own 10 buildings in New Jersey through First National Realty Management LLC. Our own office is situated in a building that I personally own in Lyndhurst, NJ. This deliberate approach was primarily aimed at creating a dependable and consistent income stream for the remainder of my life.
    </p>

    <p class="heading-strong"><strong>How do you associate yourself with the tag serial investor/entrepreneur?</strong></p>
    <p>
        Becoming a serial investor was never part of my initial intentions or plans. However, as one business venture led to another, I seized the opportunities that presented themselves along the way. While remaining active in the real estate sector, I also took on the role of a key founder in Indus American Bank, overseeing its expansion to six branches across New Jersey and New York.
    </p>
    <p>
        Exploring various industries, I ventured into the restaurant business by establishing Mantra in Paramus, NJ. Additionally, my involvement extended to vSplash TechLabs, a company in which I played a part. In 2011, I embarked on the creation of BuzzBoard, an intelligence and analytics platform tailored for small and medium-sized businesses, where I continue to serve as chairman.
    </p>
    <p>
        Moreover, I played a role in launching multiple companies in India, subsequently entrusting their management to capable individuals. This diverse portfolio of investments reflects my willingness to embrace new opportunities and support entrepreneurial endeavors.
    </p>
    
