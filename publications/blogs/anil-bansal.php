    <p class="heading-strong"><strong>Had you not been an investor/entrepreneur, what would you have been rather and why?</strong></p>
    <p>
        From an early age, my inclination was always towards business. However, I ended up pursuing engineering primarily because of my academic excellence. Throughout my journey, I never harbored any doubts about achieving success. If I hadn't embarked on my own entrepreneurial endeavors, I have no doubt that I would have risen to the ranks of a C-level executive within a prominent corporation.
    </p>

    <p class="heading-strong"><strong>How do you think the Gangwal School of Medical Sciences & Technology is going to shape the future of IITK?</strong></p>
    <p>
        I am delighted to observe the remarkable progress of IIT Kanpur, led by the capable guidance of Prof. Abhay Karandikar. I firmly believe that the establishment of the unique School of Medical Sciences & Technology, accompanied by a 500-bed Super-Specialty Hospital, will revolutionize medical research and innovation in India. This significant endeavor holds the potential to enhance the quality of life for numerous individuals and create ample employment opportunities, benefiting a vast number of Indians.
    </p>

    <p class="heading-strong"><strong>What has been your motivation behind giving back to your Alma Mater?</strong></p>
    <p>
        It is truly a gratifying experience to make a contribution to one's alma mater, and the joy multiplies when the cause is as noble as the establishment of the Gangwal School of Medical Sciences & Technology at IIT-K. My wife, Kumud Bansal, and I are overjoyed to be part of this remarkable journey and are pleased to contribute to the new school.
    </p>
    
