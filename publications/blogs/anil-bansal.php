    <p>
        Mr. Anil Bansal (BT/ME/1977), an eminent alumnus, recipient of the Distinguished Alumnus Award 2023 and one of the top benefactors of IITK, is a serial entrepreneur currently serving as The President at First National Realty Management. Mr. Bansal is known for his humanitarian nature, leadership skills and philanthropy. He was selected as 'The South Asian Times Person of the Year 2022'. In an interview with the DoRA office, he talks about his life journey, aspirations, challenges, and how IITK shaped his personality.
    </p>

    <p class="heading-strong"><strong>Tell us about your early childhood.</strong></p>
    <p>
        I had the privilege of growing up in a loving and close-knit family. Our main focus was always on cultivating happiness, and we were fortunate to have a wide circle of friends. During my childhood, I once asked my father a thought-provoking question: whether we were rich or poor. His response left a lasting impression on me. He explained that if your hand is held with the palm facing downward, you are considered rich, but if your hand is palm up, it doesn't matter how much you possess, you are poor. This perspective instilled in me a lifelong desire to be a giving and generous person.
    </p>
    <p>
        While financial success came later in my life, I learned the value of self-sufficiency from a young age. Starting at twelve years old, I would independently earn money to support myself. I engaged in entrepreneurial ventures like purchasing wholesale firecrackers and Holi colors, which I then sold to other children on the streets. This allowed me to earn enough to both sustain myself and enjoy playful activities.
    </p>
    <p>
        After school, I would spend a few hours at my father's shop located in the wholesale electric goods market in Chandni Chowk, Delhi. It was an enriching experience that exposed me to the world of business at a young age.
    </p>
    
