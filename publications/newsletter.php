document.addEventListener('DOMContentLoaded', function() {
    const yearList = document.getElementById('year-list-carousel');
    const yearItems = yearList.querySelectorAll('li');
    const leftArrow = document.querySelector('.left-arrow');
    const rightArrow = document.querySelector('.right-arrow');
    const newsletterItems = document.querySelectorAll('.newsletter-item');

    const yearsPerPage = 5; // Number of years to show at once
    let currentPage = 0;

    function updateYearCarousel() {
        const firstItem = yearItems[0];
        if (!firstItem) return;
        const itemStyle = getComputedStyle(firstItem);
        const itemWidth = firstItem.offsetWidth;
        const gap = parseFloat(getComputedStyle(yearList).gap) || 0;

        const totalItemWidth = itemWidth + gap;

        const offset = -currentPage * totalItemWidth * yearsPerPage;
        yearList.style.transform = `translateX(${offset}px)`;

        leftArrow.style.display = currentPage === 0 ? 'none' : 'flex';
        const maxPages = Math.ceil(yearItems.length / yearsPerPage) - 1;
        rightArrow.style.display = currentPage >= maxPages ? 'none' : 'flex';
    }

    function filterNewsletters(selectedYear) {
        newsletterItems.forEach(newsletter => {
        if (newsletter.getAttribute('data-year') === selectedYear) {
            newsletter.style.display = 'flex';
        } else {
            newsletter.style.display = 'none';
        }
    });
}

yearItems.forEach(item => {
    item.addEventListener('click', function() {
        yearItems.forEach(i => i.classList.remove('active'));
        this.classList.add('active');
        const selectedYear = this.getAttribute('data-year');
        filterNewsletters(selectedYear);
    });
});

leftArrow.addEventListener('click', () => {
    if (currentPage > 0) {
        currentPage--;
        updateYearCarousel();
    }
});

rightArrow.addEventListener('click', () => {
    const maxPages = Math.ceil(yearItems.length / yearsPerPage) - 1;
    if (currentPage < maxPages) {
        currentPage++;
        updateYearCarousel();
    }
});

// Initial setup
updateYearCarousel();
if (yearItems.length > 0) {
    yearItems[0].classList.add('active');
    filterNewsletters(yearItems[0].getAttribute('data-year'));
}

window.addEventListener('resize', () => {
    const activeYearIndex = Array.from(yearItems).findIndex(item => item.classList.contains('active'));
    if (activeYearIndex !== -1) {
        currentPage = Math.floor(activeYearIndex / yearsPerPage);
    } else {
        currentPage = 0;
    }
    updateYearCarousel();
});
});
</script>

