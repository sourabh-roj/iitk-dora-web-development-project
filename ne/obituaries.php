window.addEventListener('load', () => {
  document.querySelectorAll('.news-img-gallery img').forEach(img => {
    if (img.complete) {
      img.classList.add('loaded');
    } else {
      img.addEventListener('load', () => {
        img.classList.add('loaded');
      });
    }
  });
});


const observer = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.style.opacity = 1;
      entry.target.style.transform = 'translateY(0)';
    }
  });
}, { threshold: 0.1 });

document.querySelectorAll('.news-card').forEach(card => {
  card.style.opacity = 0;
  card.style.transform = 'translateY(20px)';
  card.style.transition = 'opacity 0.6s ease, transform 0.4s ease';
  observer.observe(card);
});


const viewAllBtn = document.getElementById('viewAllBtn');
const hiddenItems = document.querySelectorAll('.hidden-item');
let isExpanded = false;

viewAllBtn.addEventListener('click', () => {
  isExpanded = !isExpanded;
  hiddenItems.forEach(item => {
    if (isExpanded) {
      item.style.display = 'block';
      item.style.animation = 'slideDown 0.3s ease forwards';
    } else {
      item.style.animation = 'slideUp 0.3s ease forwards';
      setTimeout(() => {
        if (!isExpanded) item.style.display = 'none';
      }, 300);
    }
  });

  const btnText = viewAllBtn.querySelector('.btn-text');
  const btnIcon = viewAllBtn.querySelector('.btn-icon');
  btnText.textContent = isExpanded ? 'Show Less' : 'View All';
  btnIcon.textContent = isExpanded ? '▲' : '▼';
  btnIcon.style.transform = isExpanded ? 'rotate(180deg)' : 'rotate(0deg)';
});


function scrollToCenter(targetId) {
  const targetElement = document.getElementById(targetId);
  if (targetElement) {
    const elementRect = targetElement.getBoundingClientRect();
    const elementTop = elementRect.top + window.pageYOffset;
    const elementHeight = elementRect.height;
    const windowHeight = window.innerHeight;
    const offsetTop = elementTop - (windowHeight / 2) + (elementHeight / 2);

    window.scrollTo({
      top: Math.max(0, offsetTop),
      behavior: 'smooth'
    });


    targetElement.classList.add('highlighted');
    setTimeout(() => {
      targetElement.classList.remove('highlighted');
    }, 2000);
  }
}


document.querySelectorAll('.memorial-link').forEach(link => {
  link.addEventListener('click', (e) => {
    e.preventDefault();
    const targetId = link.getAttribute('href').substring(1);
    scrollToCenter(targetId);


    document.querySelectorAll('.memorial-link').forEach(l => l.parentElement.classList.remove('active'));
    link.parentElement.classList.add('active');
  });
});


const yearFilter = document.getElementById('yearFilter');
yearFilter.addEventListener('change', function() {
  const selectedYear = this.value;
  document.querySelectorAll('.memorial-list li').forEach(li => {
    if (selectedYear === 'all') {
      li.style.display = '';
    } else {
      if (li.getAttribute('data-year') === selectedYear) {
        li.style.display = '';
      } else {
        li.style.display = 'none';
      }
    }
  });
  if (selectedYear === 'all') {
    viewAllBtn.style.display = 'flex';
  } else {
    viewAllBtn.style.display = 'none';
  }

  if (selectedYear !== 'all') {
    isExpanded = false;
    hiddenItems.forEach(item => {
      item.style.display = 'none';
    });
    const btnText = viewAllBtn.querySelector('.btn-text');
    const btnIcon = viewAllBtn.querySelector('.btn-icon');
    btnText.textContent = 'View All';
    btnIcon.textContent = '▼';
    btnIcon.style.transform = 'rotate(0deg)';
  }
});

if (yearFilter.value !== 'all') {
  viewAllBtn.style.display = 'none';
} else {
  viewAllBtn.style.display = 'flex';
}

</script>
