         <p>
            Prayas is an endeavor of IIT Kanpur students, focused on providing education to the marginalized children from in and around IIT Kanpur. It is currently under the Presidential Council in the Students’ Gymkhana, IITK. It’s a volunteer service to promote ‘Education for All’, ensuring inclusive and equitable quality education and promoting learning opportunities for all. <br><br>
            In its 21st year, Prayas caters to the different needs of students from the marginalized section of society, enrolled in formal schooling systems. Around 75 students enrolled in April 2021 in Classes IV to XII. Prayas has an active volunteer base of around 50 members from the IITK student community. <br><br>
            During the second wave of the Covid-19 pandemic, the primary focus of Prayas had been to provide a smooth transition to online education, in which it was quite successful. Prayas took different measures (in the form of advisory and monetary help) to make sure that no student from class IX-XII discontinued their studies due to the constraints imposed by the lockdown. Teaching could not happen without knowing the kind of lives students are living and hence it ensured that the families of our students had enough food ration during the lockdown. Moreover, the enthusiasm and dedication of the volunteers in these challenging times has been a feat in itself.
