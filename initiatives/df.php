                  <h2>National Information Centre of Earthquake Engineering</h2>
                  <p>The 12th National Workshop for UG students of architecture was held at IIT Kanpur from 1-7 July, 2019, in which 60 students who had completed six semesters of studies in their respective architecture programmes, from 14 institutes representing 12 cities from all over India, participated in this 7-day workshop. Ms. Annanya Jha, National Institute of Technology, Raipur, and Ms. Sayad Kashaf Fatima, Dr. D.Y. Patil College of Architecture, Navi Mumbai secured first positions. A 10-day (June 27-July 06, 2019) workshop on Earthquake Engineering Literature Survey was conducted in which postgraduate students from engineering colleges across India participated. To spread the message of seismic safety among school children, who are the future builders of the country, NICEE has been organizing an Inter-school Quiz based on its most popular publication, Earthquake Tips, since 2009. This year there was an overwhelming response from schools for registration for the quiz.</p>
                  <p>Forty-eight schools from thirty cities participated in the quiz. The schools were given 50 copies of the Tips, to be used for selecting a team of four participants from each school from Class VIII to Class XI. In the final round of the quiz held on January 26, 2020, seven schools participated - DAV, Ludhiana; Centre Point School, Nagpur; DPS Gandhidham; Nehru World School, Ghaziabad; Emerald Heights International School, Indore; Smt. Sulochanadevi Singhania School, Thane and Sheiling House School, Kanpur.</p>
