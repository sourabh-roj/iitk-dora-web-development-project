        <p>Throughout the year, DoRA's team organizes events like reunions, alumni day, and Foundation Day to connect with the accomplished alumni network of IITK. These events span the globe, helping alumni rekindle the bond with their alma mater. IIT Kanpur graduates go on to make significant contributions in academia, industry, entrepreneurship, and government. Their achievements serve as a constant inspiration for current students.</p>
        <p>DoRA team's efforts have resulted in the establishment of over 149 scholarships, 54 Faculty Chairs, and numerous awards. The institute's infrastructure has grown significantly, along with the creation of new departments and centres.</p>
        <p>IIT Kanpur is committed to continuous improvement. New ventures like the departments of Sustainable Energy Engineering, Cognitive Science, Design, and Space Sciences & Astronomy, Kotak School of Sustainability along with the Gangwal School of Medical Sciences and Technology, have been established. To support these initiatives, DoRA office seeks continued alumni contributions. The institute has seen a remarkable rise in donations, with an ambitious goal of reaching over Rs. 750 crores in the next three years (July 2024 - July 2027). This funding will be crucial in supporting the endeavours of the institute.</p>
        <p>I express my deepest gratitude to the alumni for their encouragement and look forward to maintaining strong connections. The message is clear: let's work together to push boundaries, innovate, and expand IIT Kanpur's reach. We value your unwavering support, creative ideas, and constructive feedback.</p>
