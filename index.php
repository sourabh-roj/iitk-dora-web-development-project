                <p>The DoRA office plays a vital role in IIT Kanpur's success. It bridges the gap between the institute and its alumni, fostering strong connections. The mission is two-fold: to strengthen these bonds and secure resources to fuel the institute's long-term goals. To professionalize alumni engagement, the institute established the IITK Development Foundation (IITK DF) in 2020, led by CEO Mr. Kapil Kaul. An extended arm of the DoRA office, IITK DF's team works with alumni, corporations, and philanthropists to raise funds.</p>
